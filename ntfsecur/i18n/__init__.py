"""
=============================================================================
ntfsecur.i18n  â€“  Internationalisation module
=============================================================================
Neutral-key translation system supporting EN / PL locales.

Usage:
    from ntfsecur.i18n import t, set_locale, get_locale

    t("admin.required")          # uses active locale
    t("admin.required", "en")    # explicit locale override

    set_locale("pl")             # switch runtime locale
    get_locale()                 # â†’ "en" | "pl"

Key convention:
    "<module>.<identifier>"  e.g.  "common.cancel", "bitlocker.enable"
    Falls back to the key itself if a translation is missing,
    so the application degrades gracefully on incomplete catalogs.
=============================================================================
"""

from __future__ import annotations

__all__ = ["t", "set_locale", "get_locale", "SUPPORTED_LOCALES"]

SUPPORTED_LOCALES: tuple[str, ...] = ("en", "pl")

# ---------------------------------------------------------------------------
#  Active locale â€“ thread-safe via module-level variable
#  (single-process tkinter app; no need for threading.local)
# ---------------------------------------------------------------------------
_LOCALE: str = "en"


def set_locale(locale: str) -> None:
    """Switch the active UI locale. Raises ValueError for unsupported codes."""
    global _LOCALE
    if locale not in SUPPORTED_LOCALES:
        raise ValueError(
            f"Unsupported locale: {locale!r}. Supported: {SUPPORTED_LOCALES}"
        )
    _LOCALE = locale


def get_locale() -> str:
    """Return the currently active locale code ('en' or 'pl')."""
    return _LOCALE


def t(key: str, locale: str | None = None) -> str:
    """
    Translate *key* into the requested locale.

    Parameters
    ----------
    key    : Neutral string identifier, e.g. ``"common.cancel"``.
             For backward-compatibility, Polish source strings are also
             accepted as keys and resolved via the legacy alias table.
    locale : Override locale for this call only.  If *None* the global
             locale set by :func:`set_locale` is used.

    Returns
    -------
    str
        Translated string, or *key* unchanged if no translation is found.
    """
    target = locale or _LOCALE
    catalog = _CATALOGS.get(target, {})
    if key in catalog:
        return catalog[key]
    # Backward-compat: try legacy Polish-string keys
    if target == "en" and key in _LEGACY_EN:
        return _LEGACY_EN[key]
    return key


